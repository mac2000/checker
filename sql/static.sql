DROP TABLE IF EXISTS `google_languages`;
DROP TABLE IF EXISTS `google_countries`;

CREATE TABLE IF NOT EXISTS `google_languages` (
	`lr` VARCHAR(10) NOT NULL PRIMARY KEY,
	`label` VARCHAR(255) NOT NULL UNIQUE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

CREATE TABLE IF NOT EXISTS `google_countries` (
	`cr` VARCHAR(10) NOT NULL PRIMARY KEY,
	`label` VARCHAR(255) NOT NULL UNIQUE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

